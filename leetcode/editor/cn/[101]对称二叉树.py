class Solution(object):
    def isSymmetric(self, root):
        """
        :type root: TreeNode
        :rtype: bool
        """
        if not root:
            return None
        return self.check(root, root)

    def check(self, p, q):
        # é€šè¿‡ã€ŒåŒæ­¥ç§»åŠ¨ã€ä¸¤ä¸ªæŒ‡é’ˆçš„æ–¹æ³•æ¥éå†è¿™æ£µæ ‘ï¼ŒpæŒ‡é’ˆå’ŒqæŒ‡é’ˆä¸€å¼€å§‹éƒ½æŒ‡å‘è¿™æ£µæ ‘çš„æ ¹ï¼Œéšåpå³ç§»æ—¶ï¼Œqå·¦ç§»ï¼Œpå·¦ç§»æ—¶ï¼Œqå³ç§»
        # æ¯æ¬¡æ£€æŸ¥å½“å‰på’ŒqèŠ‚ç‚¹çš„å€¼æ˜¯å¦ç›¸ç­‰ï¼Œå¦‚æœç›¸ç­‰å†åˆ¤æ–­å·¦å³å­æ ‘æ˜¯å¦å¯¹ç§°
        if not p and not q:
            return True
        elif not p or not q:
            return False
        return p.val == q.val and self.check(p.left, q.right) and self.check(p.right, q.left)
