class Solution:
    def strToInt(self, str: str) -> int:
        digits = []
        if not str:
            return 0
        for i in str:
            if i == " " and len(digits) == 0:
                continue

            if i == "-" and len(digits) == 0:
                digits.append(i)
                continue
            elif i == "-" and len(digits) != 0:
                break

            if i == "+" and len(digits) == 0:
                digits.append(i)
                continue
            elif i == "+" and len(digits) != 0:
                break

            if i.isdigit():
                digits.append(i)
            else:
                break
        if len(digits) == 0:
            return 0
        if len(digits) == 1 and (digits[0] == "-" or digits[0] == "+"):
            return 0
        out = int("".join(digits))
        if out > 2**31-1:
            out = 2**31-1
        elif out < -2**31:
            out = -2**31
        return out
