class Solution(object):
    def isValidSudoku(self, board):
        """
        :type board: List[List[str]]
        :rtype: bool
        """
        lines_dict = {}
        rows_dict = {}
        block_dict = {0:{}, 1:{}, 2:{}, 3:{}, 4:{}, 5:{}, 6:{}, 7:{}, 8:{}}
        for index, line in enumerate(board):
            lines_dict[index] = {}
            for i in line:
                if lines_dict[index].get(i):
                    lines_dict[index][i] += 1
                    if i != ".":
                        return False
                else:
                    lines_dict[index][i] = 1
        for index, colomn in enumerate(board[0]):
            rows_dict[index] = {}
            for j in range(len(board)):
                if rows_dict[index].get(board[j][index]):
                    rows_dict[index][board[j][index]] += 1
                    if board[j][index] != ".":
                        return False
                else:
                    rows_dict[index][board[j][index]] = 1
        for i in range(len(board)):
            for j in range(len(board[0])):
                block_num = int(j/3) + int(i/3)*3
                if block_dict[block_num].get(board[i][j]):
                    block_dict[block_num][board[i][j]] += 1
                    if board[i][j] != ".":
                        return False
                else:
                    block_dict[block_num][board[i][j]] = 1
        return True
